function dx = odelyap(t,x,u,A,B,g1,g2)

dx(1) = A(1,1)*x(1) + A(1,2)*x(2) + B(1)*u(t);
dx(2) = A(2,1)*x(1) + A(2,2)*x(2) + B(2)*u(t);
dx(3) = x(5)*x(3) + x(6)*x(4) + x(9)*u(t);
dx(4) = x(7)*x(3) + x(8)*x(4) + x(10)*u(t);
dx(5) = g1*x(3)*(x(1)-x(3));
dx(6) = g1*x(4)*(x(1)-x(3));
dx(7) = g1*x(3)*(x(2)-x(4));
dx(8) = g1*x(4)*(x(2)-x(4));
dx(9) = g2*u(t)*(x(1)-x(3));
dx(10) = g2*u(t)*(x(2)-x(4));

dx = dx';
end
